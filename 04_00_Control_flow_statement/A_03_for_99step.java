public class A_03_for_99step {
    public static void main(String[] args) {
        System.out.println("print 99 steps!!");
        for (int step1=2;step1<10;step1++) {
            for (int step2=2;step2<10;step2++) {
                System.out.println(step1+"X"+step2+"="+(step1*step2));
            }
        }
    }

}
