public class first_arithmetic_operator {
    public static void main(String[] args) {
        int a=10;
		int b=3;
        //Interinteger operations show integer results.
		System.out.println(a+b);
		System.out.println(a*b);
		System.out.println(a-b);
		System.out.println(a/b);
        System.out.println(a%b);
        //Integer and real number operations show real number results.
        double DoubleB=3;
		System.out.println(a/DoubleB); 
		System.out.println(a%DoubleB); 
		System.out.println(DoubleB%b);
    }
}
